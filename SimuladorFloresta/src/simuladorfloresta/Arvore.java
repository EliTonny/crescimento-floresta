
package simuladorfloresta;

public class Arvore {
    private int agua;
    private int luz;
    private int saisMinerais;

    public int getAgua() {
        return agua;
    }

    public void setAgua(int agua) {
        this.agua = agua;
    }

    public int getLuz() {
        return luz;
    }

    public void setLuz(int luz) {
        this.luz = luz;
    }

    public int getSaisMinerais() {
        return saisMinerais;
    }

    public void setSaisMinerais(int saisMinerais) {
        this.saisMinerais = saisMinerais;
    }

    public int getEnergia() {
        return energia;
    }

    public void setEnergia(int energia) {
        this.energia = energia;
    }
    private int energia;
}
